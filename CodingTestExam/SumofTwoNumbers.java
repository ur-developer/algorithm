package algorithm;

public class SumofTwoNumbers {
	public static int calculatieSum(int a, int b) {
		return a + b;
	
	}
	
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 5;
		int sum = calculatieSum(num1, num2);
		System.out.println("Sum of " + num1 + " and " + num2 + " is " + sum);
	}
}
